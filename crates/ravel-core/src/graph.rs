        // Two disconnected chains: 1→2, 3→4
        let g = Graph::new()
            .add_node(make_node(1))
            .unwrap()
            .add_node(make_node(2))
            .unwrap()
            .add_node(make_node(3))
            .unwrap()
            .add_node(make_node(4))
            .unwrap();
        let g = g
            .add_edge(
                EdgeId::new(1),
                NodeId::new(1),
                OutputPortIndex(0),
                NodeId::new(2),
                InputPortIndex(0),
            )
            .unwrap()
            .add_edge(
                EdgeId::new(2),
                NodeId::new(3),
                OutputPortIndex(0),
                NodeId::new(4),
                InputPortIndex(0),
            )
            .unwrap();

        let order = g.topological_sort().unwrap();
        assert_eq!(order.len(), 4);
        // 1 before 2, 3 before 4
        let pos = |id: u64| order.iter().position(|n| *n == NodeId::new(id)).unwrap();
        assert!(pos(1) < pos(2));
        assert!(pos(3) < pos(4));
