/// A directed edge connecting one output port to one input port.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct Edge {
    pub id: EdgeId,
    pub source: NodeId,
    pub source_port: OutputPortIndex,
    pub target: NodeId,
    pub target_port: InputPortIndex,
}

